package Classes;

public class Minion {
    int mana;
    int health;
    int attackDamage;
    String description;
    String colors;
    String name;

    public Minion(int mana, int health, int attackDamage, String description, String colors, String name) {
        this.mana = mana;
        this.health = health;
        this.attackDamage = attackDamage;
        this.description = description;
        this.colors = colors;
        this.name = name;
    }
}
